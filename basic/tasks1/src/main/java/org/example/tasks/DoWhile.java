package org.example.tasks;

public class DoWhile {
    public static void main(String[] args) {
        /* Stwórz pętlę do while od 0 do 20
        i wyświetl tylko i wyłącznie nieparzyste liczby
         */
        int i = 0;
        do {
            System.out.println(i);
            i++;
        }
        while (i <= 20);
    }
}
